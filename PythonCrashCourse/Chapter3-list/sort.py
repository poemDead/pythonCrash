# sort()永久排序
cars = ['bmw', 'audi', 'toyota', 'subaru'] 
cars.sort()
print(cars)
# reverse=True反向排序
cars = ['bmw', 'audi', 'toyota', 'subaru']
cars.sort(reverse=True)
print(cars)

# sorted临时排序
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(sorted(cars))
print(cars)

# reverse反向打印
cars = ['bmw', 'audi', 'toyota', 'subaru']
print(cars)

cars.reverse()
print(cars)

